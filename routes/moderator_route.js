const express = require('express')

const {addModerator, allModerators, editModerator, updateModerator, deleteModerator} = require('./../controllers/moderator_cnt')

const router = express.Router()

router.route('/').get(allModerators).post(addModerator)

router.route('/edit/:id').get(editModerator).post(updateModerator);

router.route('/delete/:id').get(deleteModerator)

module.exports = router